class Calculator():

    def addition(self, a, b):
        return a + b

    def addition3(self, a, b, c):
        return a + b + c
    
    def subtraction(self, a, b):
        if (a>b):
            return a - b
        else:
            return a - b
    
    def multiplication(self, a, b):
        return a * b
    
    def division(self, a, b):
        return a / b